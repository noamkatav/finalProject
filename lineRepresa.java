package finalProject;

public class lineRepresa {
	
	public int fi;
	public int fj;
	public int li;
	public int lj;
	
	public lineRepresa(int fi,int fj, int li, int lj)
	{
		this.fi = fi;
		this.fj = fj;
		this.li = li;
		this.lj = lj;
		
	}
	public lineRepresa() {

		this.fi = -1;
		this.fj = -1;
		this.li = -1;
		this.lj = -1;
		
	}

}
